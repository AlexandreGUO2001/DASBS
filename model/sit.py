def get_sit_model(args, require_time):
    return SiT(img_size=args.L, vocab_size=args.vocab_size, hidden_size=args.model.hidden_size, 
               depth=args.model.n_blocks, num_heads=args.model.n_heads, mlp_ratio=4,
               require_time=require_time)
